/**
 * 生成词库
 * 将所有词汇组合并补充到2048个
 */
export function generateWordlist(): string[] {
  const wordlist: string[] = [];

  // 1. 添加同济建筑
  wordlist.push(...TONGJI_BUILDINGS);

  // 2. 添加上海地标
  wordlist.push(...SHANGHAI_LANDMARKS);

  // 3. 生成补充词汇（使用组合方式）
  const prefixes = [
    '东', '南', '西', '北', '中',
    '新', '老', '大', '小', '高',
    '低', '长', '短', '宽', '窄',
    '明', '暗', '红', '绿', '蓝',
    '黄', '白', '黑', '金', '银',
    '春', '夏', '秋', '冬', '晨',
    '午', '晚', '夜', '朝', '暮'
  ];

  const buildings = [
    '楼', '馆', '院', '堂', '阁',
    '亭', '台', '榭', '轩', '斋',
    '室', '舍', '屋', '房', '厅',
    '殿', '宫', '塔', '桥', '门',
    '墙', '窗', '廊', '道', '径',
    '园', '林', '苑', '圃', '场',
    '坊', '街', '巷', '路', '道',
    '广场', '公园', '花园', '庭院', '别墅'
  ];

  const suffixes = [
    '一号', '二号', '三号', '四号', '五号',
    '六号', '七号', '八号', '九号', '十号',
    '甲', '乙', '丙', '丁', '戊',
    '东区', '南区', '西区', '北区', '中区',
    '新区', '老区', '主楼', '副楼', '附楼'
  ];

  // 生成组合词汇
  for (const prefix of prefixes) {
    for (const building of buildings) {
      if (wordlist.length >= 2048) break;
      wordlist.push(`${prefix}${building}`);
    }
    if (wordlist.length >= 2048) break;
  }

  // 如果还不够，继续生成三字组合
  if (wordlist.length < 2048) {
    for (const prefix of prefixes) {
      for (const building of buildings) {
        for (const suffix of suffixes) {
          if (wordlist.length >= 2048) break;
          wordlist.push(`${prefix}${building}${suffix}`);
        }
        if (wordlist.length >= 2048) break;
      }
      if (wordlist.length >= 2048) break;
    }
  }

  // 确保正好2048个
  return wordlist.slice(0, 2048);
}

/**
 * 获取词库统计信息
 */
export function getWordlistStats() {
  const wordlist = generateWordlist();
  return {
    total: wordlist.length,
    tongjiBuildings: TONGJI_BUILDINGS.length,
    shanghaiLandmarks: SHANGHAI_LANDMARKS.length,
    generated: wordlist.length - TONGJI_BUILDINGS.length - SHANGHAI_LANDMARKS.length
  };
}
